let myDate = new Date()
console.log(myDate.toDateString());//--> Fri Feb 16 2024
console.log(myDate.toISOString());//--> 2024-02-16T02:40:30.079Z
console.log(myDate.toLocaleDateString());//--> 2/16/2024
console.log(myDate.toLocaleString());//--> 2/16/2024, 2:40:30 AM
console.log(myDate.toLocaleTimeString());//--> 2:40:30 AM